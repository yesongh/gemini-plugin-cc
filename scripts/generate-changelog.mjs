#!/usr/bin/env node
import { execFileSync } from "node:child_process";
import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { prependEntry } from "./lib/changelog.mjs";

const REPO_ROOT = new URL("..", import.meta.url);
const CHANGELOG = new URL("../CHANGELOG.md", import.meta.url);

const NEW_VERSION = process.env.NEW_VERSION;
const API_KEY = process.env.ANTHROPIC_API_KEY;
const MODEL = process.env.CHANGELOG_MODEL ?? "claude-sonnet-4-6";

if (!NEW_VERSION) throw new Error("NEW_VERSION env var is required");
if (!API_KEY) throw new Error("ANTHROPIC_API_KEY env var is required");

const sh = (cmd, args) =>
  execFileSync(cmd, args, { encoding: "utf8", cwd: REPO_ROOT }).trim();

const lastTag = (() => {
  try {
    return sh("git", ["describe", "--tags", "--abbrev=0"]);
  } catch {
    return null;
  }
})();

const range = lastTag ? `${lastTag}..HEAD` : "HEAD";
const commits = sh("git", ["log", range, "--pretty=format:- %s (%h)"]);

const prArgs = [
  "pr",
  "list",
  "--state",
  "merged",
  "--base",
  "main",
  "--limit",
  "100",
  "--json",
  "number,title,author,labels",
];
if (lastTag) {
  const tagDate = sh("git", ["log", "-1", "--format=%cI", lastTag]);
  prArgs.push("--search", `merged:>=${tagDate}`);
}
const prsJson = sh("gh", prArgs);

const prompt = `You are writing a CHANGELOG entry in Keep-a-Changelog format for version ${NEW_VERSION} of a Claude Code plugin (gemini-plugin-cc).

Previous version: ${lastTag ?? "(first release)"}
New version: ${NEW_VERSION}

Commits since previous version:
${commits}

Merged pull requests since previous version:
${prsJson}

Write the CHANGELOG section as markdown. Rules:
- Start with '## [${NEW_VERSION}] - ${new Date().toISOString().slice(0, 10)}' as the heading
- Group entries under '### Added', '### Changed', '### Fixed', '### Removed' — omit sections that are empty
- Entries are short, user-facing, past-tense or imperative. No implementation detail.
- Reference PRs as '(#N)' at the end of the line when a PR exists
- Credit external contributors with '(thanks @username)' for PRs not authored by abiswas97
- No trailing commentary, no "generated by" footer, no preamble. Only the markdown section.
- Match a terse, human tone. Do not pad with filler.`;

const response = await fetch("https://api.anthropic.com/v1/messages", {
  method: "POST",
  headers: {
    "content-type": "application/json",
    "x-api-key": API_KEY,
    "anthropic-version": "2023-06-01",
  },
  body: JSON.stringify({
    model: MODEL,
    max_tokens: 2000,
    messages: [{ role: "user", content: prompt }],
  }),
});

if (!response.ok) {
  throw new Error(`Anthropic API ${response.status}: ${await response.text()}`);
}

const body = await response.json();
const raw = body.content
  .filter((block) => block.type === "text")
  .map((block) => block.text)
  .join("")
  .trim();

const entry = raw
  .replace(/^```(?:markdown|md)?\s*\n/, "")
  .replace(/\n```\s*$/, "")
  .trim();

const existing = existsSync(CHANGELOG) ? readFileSync(CHANGELOG, "utf8") : "";
writeFileSync(CHANGELOG, prependEntry(existing, entry));

console.log(
  `Wrote CHANGELOG entry for v${NEW_VERSION} (${entry.length} chars).`,
);
